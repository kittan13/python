int main()
{
    int year;
    size_t i;

    printf("西暦 = "); scanf("%d", &year);

    for (i = 0; i < ARRAY_SIZE(japan_era); i++){
        if ((japan_era[i].begin <= year) && (year <= japan_era[i].end)){
            if (japan_era[i].base == year){
                printf("%s元年", japan_era[i].name);
            } else {
                printf("%s%d年", japan_era[i].name, year - japan_era[i].base + 1);
            }
            if (japan_era[i].comment){
                printf(" (%s)", japan_era[i].comment);
            }
            putchar('\n');
        }
    }

    return 0;
}